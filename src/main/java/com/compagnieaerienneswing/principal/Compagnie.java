    // Insert a new airline into the database
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        try{
            String compagnie = textCompagnie.getText();

            if (isNullOrEmpty(compagnie))
                throw new Exception("Le champs compagnie est requis.");

            CompagnieDAO compagnieDao = new CompagnieDAO();
            String sql = String.format("SELECT * FROM compagnie WHERE nom LIKE '%s'", compagnie);
            ArrayList<Compagnie> compagnieList = compagnieDao.select(sql);

            if (compagnieList.size() > 0)
                throw new Exception("La compagnie existe déjà dans la base de données !");

            MySQL mySql = new MySQL();
            sql = String.format("INSERT INTO compagnie VALUES (default, '%s')", compagnie);
            String insertCompagnie = mySql.insert(sql);

            if (insertCompagnie != "OK")
                throw new Exception("Une erreur est survenue ! Le pays n'a pas été ajouté.");

            JOptionPane.showMessageDialog(null, "La compagnie a été ajoutée !");
            reset();
            loadCompagniesData();
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    // Delete an existing airline from the database
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            int row = tableCompagnie.getSelectedRow();
            int idCol = tableCompagnie.getColumn("ID").getModelIndex();
            String id = tableCompagnie.getModel().getValueAt(row, idCol).toString();

            MySQL mySql = new MySQL();
            String sql = String.format("DELETE FROM compagnie WHERE idcompagnie = %s", id);
            String str = mySql.delete(sql);

            if(str != "OK"){
                throw new Exception(str);
            }

            reset();
            loadCompagniesData();
            JOptionPane.showMessageDialog(null, "La compagnie a correctement été supprimée !");
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    // update an existing airline from the database
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            int row = tableCompagnie.getSelectedRow();
            int idCol = tableCompagnie.getColumn("ID").getModelIndex();
            int nomCol = tableCompagnie.getColumn("Nom").getModelIndex();

            String id = tableCompagnie.getModel().getValueAt(row, idCol).toString();
            String nom = tableCompagnie.getModel().getValueAt(row, nomCol).toString();

            MySQL mySql = new MySQL();
            String sql = String.format("UPDATE compagnie SET nom = '%s' WHERE idcompagnie = %s", nom, id);
            String str = mySql.update(sql);

            if(str != "OK"){
                throw new Exception(str);
            }

            reset();
            loadCompagniesData();
            JOptionPane.showMessageDialog(null, "La compagnie a correctement été mise à jour !");
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

