    // Load table when running the program
    private void loadCompagniesData(){
        
        String query = "SELECT * FROM compagnie";
        CompagnieDAO compagnieDao = new CompagnieDAO();
        ArrayList<Compagnie> compagnieList = compagnieDao.select(query);
        
        // Add every airline informations to the table
        for (int i = 0; i < compagnieList.size(); i++){
            int id = compagnieList.get(i).getId();
            String name = compagnieList.get(i).getName();
            
            Object[] row = { id, name };
            DefaultTableModel model = (DefaultTableModel) tableCompagnie.getModel();
            model.addRow(row);
        }
    }
    
    // Reset the table
    private void reset(){
        DefaultTableModel model = (DefaultTableModel) tableCompagnie.getModel();
        int row = model.getRowCount();
        
        if (row > 0){
            for (int i = 0; i < row; i++){
                model.removeRow(0);
            }
        }
    }

